let provider;
let signer;
let contract;

// DOM Elements
const walletButton = document.getElementById("wallet-button");
const redeemForm = document.getElementById("redeem-form");

// Connect Avalanche Core Wallet Function
async function connectWallet() {
  if (window.avalanche) {
    provider = new ethers.providers.Web3Provider(window.avalanche);
    await provider.send("eth_requestAccounts", []);
    signer = provider.getSigner();
    contract = new ethers.Contract(contractAddress, contractABI, signer);

    const walletAddress = await signer.getAddress();
    walletButton.innerHTML =
      walletAddress.slice(0, 6) + "..." + walletAddress.slice(-4);
  } else {
    alert("Avalanche Core Wallet is not installed. Please install it to use this feature.");
  }
}

// Redeem Gift Card Function
async function redeemGiftCard(event) {
  event.preventDefault();

  const tokenId = document.getElementById("token-id").value;
  const physicalCode = document.getElementById("physical-code").value;
  const nfcCode = document.getElementById("nfc-code").value;
  const pin = document.getElementById("pin").value;

  try {
    const tx = await contract.redeemGiftCard(tokenId, physicalCode, pin, nfcCode);
    await tx.wait();
    alert("Gift Card Redeemed Successfully!");
  } catch (error) {
    console.error("Error redeeming gift card:", error);
    alert("Failed to redeem gift card. Please try again.");
  }
}

// Event Listeners
walletButton.addEventListener("click", connectWallet);
redeemForm.addEventListener("submit", redeemGiftCard);
